void amr_decode_test(void *arg)
{
	int  write_ret = -1;
	PCM_HANDLE_T write_hdl;
	QL_PCM_CONFIG_T pcm_config = {1, 8000, 0};
	int i = 0;

	//等待audio初始化好
	while(!ql_get_audio_state()){
		ql_rtos_task_sleep_ms(50);
	}
	ql_codec_choose(AUD_INTERNAL_CODEC, NULL);

	printf("pcm_test start ...\r\n");
	
	ql_set_audio_path_receiver();
	ql_set_volume(10);
	ql_amrnbdec_init();    //解码前的初始化
	
	write_hdl = ql_pcm_open(&pcm_config, QL_PCM_WRITE_FLAG|QL_PCM_BLOCK_FLAG);
	if (write_hdl == NULL) {
		printf("ql_pcm_open write fail\n");
		return;
	}
	ql_rtos_task_sleep_s(10);  // 开机过程中防止其他高优先级task运行 导致卡顿
	while (i<sizeof(Tab_ARMTestData)) {
			//printf("dec start\n");
			ql_amrnbdec_do(Tab_ARMTestData+i,tmp_buf);    // amr数据需要1帧帧的解码   具体长度可以根据帧头确定 一帧amr数据解码后是320字节的pcm数据
			//printf("dec end\n");
			write_ret = ql_pcm_write(write_hdl, tmp_buf, sizeof(tmp_buf));		  //PCM 播放
			i += 21;
	}
	ql_rtos_task_sleep_s(1);
	ql_pcm_close(write_hdl);
	ql_amrnbdec_deinit();
}

